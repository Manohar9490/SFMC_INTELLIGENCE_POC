CREATE TABLE IF NOT EXISTS RAW_HCP_SUBSCRIBER (
    SUBSCRIBER_KEY      VARCHAR,
    NPI                 VARCHAR,
    FIRST_NAME          VARCHAR,
    LAST_NAME           VARCHAR,
    SPECIALTY           VARCHAR,
    HCP_TIER            VARCHAR,
