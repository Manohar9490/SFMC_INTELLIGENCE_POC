    BRAND_ID            VARCHAR,
    IMPORT_DATE         VARCHAR,
    SOURCE_FILE_NAME    VARCHAR,
    LOAD_TIMESTAMP      TIMESTAMP_NTZ DEFAULT CURRENT_TIMESTAMP()
);

COPY INTO RAW_HCP_SUBSCRIBER
    (SUBSCRIBER_KEY, NPI, FIRST_NAME, LAST_NAME, SPECIALTY, HCP_TIER, LICENSE_STATE,
     INSTITUTION_NAME, CONSENT_STATUS, DO_NOT_CONTACT_FLAG, BUSINESS_UNIT_ID, BRAND_ID,
     IMPORT_DATE, SOURCE_FILE_NAME)
